-- ══ 7. groomer_shifts：所有美容師未來 21 天班表（週日標記休假）══
INSERT INTO groomer_shifts (groomer_id, store_id, work_date, start_time, end_time, is_day_off)
SELECT
  g.id AS groomer_id,
  g.store_id,
  (CURRENT_DATE + s.n)::date AS work_date,
  '09:00'::time AS start_time,
  '18:00'::time AS end_time,
  EXTRACT(DOW FROM (CURRENT_DATE + s.n)) = 0 AS is_day_off
FROM groomers g
CROSS JOIN generate_series(0, 20) AS s(n)
WHERE g.is_active = true
ON CONFLICT (groomer_id, work_date) DO NOTHING;
